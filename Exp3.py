texto = "\nAprendendo Python na disciplina de linguagem de programação."


print(texto)

#len usado para saber a quantidade de caracteres tem no texto
print(f"Tamanho do texto: {len(texto)}\n")

#a função split() foi usada para cortar o texto e o resultado foi guardado na variavel palavras
palavras = texto.split()

print(f"palavras exibidas no texto:\n {palavras}\n")

print(f"Quantidade de palavras : {len(palavras)}\n")

